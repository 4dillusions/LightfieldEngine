	{}

	LFColor::LFColor(float r, float g, float b) : a(0), r(r), g(g), b(b)
	{}

	LFColor::LFColor(const LFVector3D &color)
	{
		r = color.x;
		g = color.y;
		b = color.z;
		a = 1.0f;
	}

	LFColor & LFColor::operator=(const LFColor &value)
	{
		r = value.r;
		g = value.g;
		b = value.b;
		a = value.a;

		return *this;
	}

	LFVector3D LFColor::ConvertToFloatColorRGB() const
    {
		return LFVector3D(1 / (255 / static_cast<float>(r)), 1 / (255 / static_cast<float>(g)), 1 / (255 / static_cast<float>(b))); 
	}
